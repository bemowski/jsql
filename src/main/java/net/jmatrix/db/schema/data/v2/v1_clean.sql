
CREATE TABLE DBM_LOG( 
   ID VARCHAR(50) NOT NULL,
   TSTAMP TIMESTAMP NOT NULL,
   FILEPATH VARCHAR(500),
   HOSTNAME VARCHAR(500),
   USERNAME VARCHAR(500),
   STATUS VARCHAR(10), /* success/fail */
   NUM_ROWS INTEGER,   /* number of rows affected. */
   SQL VARCHAR(4000),
   ERROR VARCHAR(4000),
 PRIMARY KEY (ID));

CREATE TABLE DBM_VERSIONS(
   ID VARCHAR(50) NOT NULL,
   TSTAMP TIMESTAMP NOT NULL,
   FILEPATH VARCHAR(500),
   ACTION VARCHAR(20),  /* apply/rollback */
   HOSTNAME VARCHAR(500),
   USERNAME VARCHAR(500),
   VERSION VARCHAR(100) NOT NULL,
   HASH VARCHAR(200),
 PRIMARY KEY(ID));