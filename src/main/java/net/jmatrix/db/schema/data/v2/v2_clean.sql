

CREATE TABLE DBM_LOG( 
  ID VARCHAR(50) NOT NULL,
  VERSION_ID VARCHAR(50),  /* V2, nullable */
  TSTAMP INTEGER NOT NULL,
  FILEPATH VARCHAR(500),
  SUCCESS VARCHAR(1), /* <V1 Status - T/F */
  NUM_ROWS INTEGER,   /*  number of rows affected. */
  SQL VARCHAR(4000),
  ERROR VARCHAR(4000),
PRIMARY KEY (ID));

CREATE TABLE DBM_VERSIONS(
  ID VARCHAR(50) NOT NULL,
  TSTAMP INTEGER NOT NULL,
  FILEPATH VARCHAR(500),
  ACTION VARCHAR(20),     /* INIT/APPLY/ROLLBACK/MANUAL */
  ROLLBACK VARCHAR(1),    /* V2, NULLABLE - Y/N - indicating at the time of apply, if rollback existed. */
  HOSTNAME VARCHAR(500),
  HOSTUSER VARCHAR(500),  /* < V1.USERNAME */
  DBUSER VARCHAR(500),    /* V2, NULLABLE */
  VERSION VARCHAR(100) NOT NULL,
  FILE_CHECKSUM VARCHAR(50),  /* V2, NULLABLE */
  DB_CHECKSUM VARCHAR(50),    /* NULLABLE */
  NOTES VARCHAR(1000),        /* V2, NULLABLE */
  ROLLBACK_SQL VARCHAR(2000),
PRIMARY KEY(ID));
